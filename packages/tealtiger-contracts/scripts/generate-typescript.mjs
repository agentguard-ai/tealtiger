import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { compileFromFile } from 'json-schema-to-typescript';

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const packageDirectory = path.resolve(scriptDirectory, '..');
const schemaDirectory = path.join(packageDirectory, 'schemas');
const outputDirectory = path.join(packageDirectory, 'typescript', 'generated');
const checkOnly = process.argv.includes('--check');

const contracts = [
  ['action.schema.json', 'action.ts'],
  ['decision.schema.json', 'decision.ts'],
  ['approval.schema.json', 'approval.ts'],
  ['execution-receipt.schema.json', 'execution-receipt.ts'],
  ['target-capability.schema.json', 'target-capability.ts'],
];

const bannerComment = `/*
 * AUTO-GENERATED from packages/tealtiger-contracts/schemas.
 * Do not edit this file directly. Run npm run generate instead.
 */`;

let stale = false;

await mkdir(outputDirectory, { recursive: true });

for (const [schemaName, outputName] of contracts) {
  const schemaPath = path.join(schemaDirectory, schemaName);
  const outputPath = path.join(outputDirectory, outputName);
  const generated = await compileFromFile(schemaPath, {
    bannerComment,
    cwd: schemaDirectory,
    format: true,
    style: {
      bracketSpacing: true,
      printWidth: 100,
      semi: true,
      singleQuote: true,
      tabWidth: 2,
      trailingComma: 'all',
      useTabs: false,
    },
    unknownAny: true,
  });

  if (checkOnly) {
    let current = '';
    try {
      current = await readFile(outputPath, 'utf8');
    } catch {
      // Missing generated output is reported with the same actionable message.
    }

    if (current !== generated) {
      stale = true;
      console.error(`Generated TypeScript is stale: ${path.relative(packageDirectory, outputPath)}`);
    }
  } else {
    await writeFile(outputPath, generated, 'utf8');
    console.log(`Generated ${path.relative(packageDirectory, outputPath)}`);
  }
}

if (stale) {
  console.error('Run npm run generate in packages/tealtiger-contracts and commit the result.');
  process.exitCode = 1;
}
